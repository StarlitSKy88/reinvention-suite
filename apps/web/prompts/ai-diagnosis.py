# 3. Python 调用函数（用于测试）
# ============================================

def build_analysis_prompt(answers: dict) -> str:
    """构建综合分析的 prompt"""
    answer_text = ""
    for q in QUESTIONS:
        answer = answers.get(q["id"], "（未回答）")
        answer_text += f"### {q['question']}\n用户回答：{answer}\n\n"

    return ANALYSIS_PROMPT.replace("{answers}", answer_text)


